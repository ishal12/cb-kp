     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $jenisCuti = new Jenis_cuti;
        $jenisCuti->nama = $request->nama;
        $jenisCuti->limit = $request->maksHari;
        $jenisCuti->kategori = $request->pCuti;
        $jenisCuti->save();

        session()->flash('success', 'Berhasil dirubah');
        return redirect()->route('kategoriCuti.index');
    }

